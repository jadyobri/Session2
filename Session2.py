#floating point addition
x = 3.96
y = 0.24
z = x+y
print("3.96 + 0.24 =", z)
print("The type is:",type(z))

#integer difference
x = 100
y = 50
z = x-y
print("\n100 - 50 =", z)
print("The type is:",type(z))

#product of floating point number and integer
x = 96
y = 0.24
z = 96*0.24
print("\n96 * 0.24 =", z)
print("The type is:",type(z))


